package week2.session1.staticKeyword;

public class MathUtils {

    public static final double PI = 3.14;

    // Static metot
    public static int add(int a, int b) {
        return a + b;
    }

    // Static metot
    public static int multiply(int a, int b) {
        return a * b;
    }

    // Non-static metot
    public int subtract(int a, int b) {
        return a - b;
    }
}
